// ── MAIN GENERATOR ──
// ══════════════════════════════════════════════

function generate(taxData, formType) {
  return new Promise((resolve, reject) => {
  const doc = new PDFDocument({ size: 'LETTER', margins: { top: 50, bottom: 50, left: 50, right: 50 } });
  const buffers = [];
  doc.on('data', b => buffers.push(b));
  doc.on('end', () => resolve(Buffer.concat(buffers)));
  doc.on('error', reject);

  // Initialize fonts
  initializeFonts(doc);

  // ── Page 1: Form 1040 Summary ──
  const p = taxData.personal || {};
  const status = taxData.filingStatus || 'single';
  const w2s = taxData.w2s || [];
  const necs = taxData.nec1099s || [];
  const ded = taxData.deductions || {};
  const exp = taxData.expenses || {};

  // Title
  doc.font('Helvetica-Bold').fontSize(18).fill('#355ebe').text('U.S. Individual Income Tax Return', 50, 50, { align: 'center' });
  doc.fontSize(12).fill('#666').text('Form 1040 — Tax Year 2025 (Summary)', 50, 74, { align: 'center' });
  doc.fontSize(9).fill('#999').text('Generated by Tax Advisor Pro — مستشار الضرائب', 50, 90, { align: 'center' });

  let y = 115;
  y = drawLine(doc, y);

  // Personal Info
  y = drawHeader(doc, 'Personal Information — المعلومات الشخصية', y);
  y = drawRow(doc, 'Full Name', `${p.firstName || ''} ${p.lastName || ''}`.trim() || 'N/A', y);
  y = drawRow(doc, 'SSN', p.ssn ? `XXX-XX-${p.ssn.slice(-4)}` : 'N/A', y);
  y = drawRow(doc, 'Address', `${p.address || ''}, ${p.city || ''}, ${p.state || ''} ${p.zip || ''}`.trim(), y);
  y = drawRow(doc, 'Filing Status', status.toUpperCase(), y);
  y += 6;

  // W-2 Income
  y = drawHeader(doc, 'W-2 Wages — دخل الرواتب', y);
  let totalWages = 0, totalFedWithheld = 0, totalStateWithheld = 0;
  if (w2s.length > 0) {
    for (const w of w2s) {
      const wages = parseFloat(w.wages) || 0;
      const fed = parseFloat(w.fedTaxWithheld) || 0;
      const st = parseFloat(w.stateTax) || 0;
      totalWages += wages;
      totalFedWithheld += fed;
      totalStateWithheld += st;
      y = drawRow(doc, `${w.employer || 'Employer'}`, wages, y);
    }
  } else {
    y = drawRow(doc, 'No W-2 income reported', '', y);
  }
  y = drawRow(doc, 'Total W-2 Wages', totalWages, y, { bold: true });
  y = drawRow(doc, 'Federal Tax Withheld', totalFedWithheld, y);
  y += 6;

  // 1099 Income
  y = drawHeader(doc, '1099-NEC Self-Employment — دخل العمل الحر', y);
  let total1099 = 0;
  if (necs.length > 0) {
    for (const n of necs) {
      const amt = parseFloat(n.amount) || 0;
      total1099 += amt;
      y = drawRow(doc, `${n.payer || 'Client'}`, amt, y);
    }
  } else {
    y = drawRow(doc, 'No 1099-NEC income reported', '', y);
  }
  y = drawRow(doc, 'Total 1099-NEC', total1099, y, { bold: true });

  // Business expenses
  const totalExpenses = Object.values(exp).reduce((s, v) => s + (parseFloat(v) || 0), 0);
  if (totalExpenses > 0) {
    y += 4;
    y = drawRow(doc, 'Business Expenses (Schedule C)', totalExpenses, y);
  }
  const netSE = Math.max(0, total1099 - totalExpenses);
  y = drawRow(doc, 'Net Self-Employment Income', netSE, y, { bold: true });
  y += 6;

  // ── Page 2 if needed ──
  if (y > 600) { doc.addPage(); y = 50; }

  // Deductions
  y = drawHeader(doc, 'Deductions — الخصومات', y);
  const itemized = (parseFloat(ded.mortgage) || 0) + (parseFloat(ded.salt) || 0) +
    (parseFloat(ded.charity) || 0) + (parseFloat(ded.medical) || 0);
  const stdDed = calcStandardDeduction(status);
  const useItemized = itemized > stdDed;
  const totalDeduction = useItemized ? itemized : stdDed;

  y = drawRow(doc, 'Standard Deduction', stdDed, y);
  if (itemized > 0) {
    y = drawRow(doc, 'Itemized Total (mortgage, SALT, charity, medical)', itemized, y);
  }
  y = drawRow(doc, `Using: ${useItemized ? 'Itemized' : 'Standard'} Deduction`, totalDeduction, y, { bold: true });

  // Above-the-line deductions
  const aboveLine = (parseFloat(ded.ira) || 0) + (parseFloat(ded.studentLoan) || 0) +
    (parseFloat(ded.hsa) || 0) + (parseFloat(ded.seHealth) || 0);
  if (aboveLine > 0) {
    y = drawRow(doc, 'Above-the-line (IRA, student loan, HSA, SE health)', aboveLine, y);
  }
  y += 6;

  // Tax Calculation
  y = drawHeader(doc, 'Tax Calculation — حساب الضريبة', y);
  const totalIncome = totalWages + netSE;
  const seTax = total1099 > 0 ? calcSETax(netSE) : 0;
  const seDeduction = Math.round(seTax / 2 * 100) / 100;
  const agi = totalIncome - aboveLine - seDeduction;
  const taxableIncome = Math.max(0, agi - totalDeduction);
  const incomeTax = calcTax(taxableIncome, status);
  const totalTax = incomeTax + seTax;
  const totalPayments = totalFedWithheld;
  const refundOrOwe = totalPayments - totalTax;

  y = drawRow(doc, 'Total Income (W-2 + 1099)', totalIncome, y);
  y = drawRow(doc, 'Adjustments (above-the-line + ½ SE tax)', aboveLine + seDeduction, y);
  y = drawRow(doc, 'Adjusted Gross Income (AGI)', agi, y, { bold: true });
  y = drawRow(doc, `Deduction (${useItemized ? 'Itemized' : 'Standard'})`, totalDeduction, y);
  y = drawRow(doc, 'Taxable Income', taxableIncome, y, { bold: true });
  y += 4;
  y = drawRow(doc, 'Federal Income Tax', incomeTax, y);
  if (seTax > 0) y = drawRow(doc, 'Self-Employment Tax (Schedule SE)', seTax, y);
  y = drawRow(doc, 'Total Tax', totalTax, y, { bold: true });
  y += 4;
  y = drawRow(doc, 'Federal Tax Withheld (from W-2s)', totalFedWithheld, y);
  y += 4;
  y = drawLine(doc, y);

  // Big result
  const isRefund = refundOrOwe >= 0;
  const resultText = isRefund ? 'ESTIMATED REFUND — المبلغ المسترد' : 'ESTIMATED TAX OWED — الضريبة المستحقة';

  // Try to use Arabic-compatible font for result text
  try {
    doc.font('ArabicFallback').fontSize(16)
      .fill(isRefund ? '#007a5e' : '#d52b1d')
      .text(resultText, 50, y + 4, { align: 'center' });
  } catch (err) {
    doc.font('Helvetica-Bold').fontSize(16)
      .fill(isRefund ? '#007a5e' : '#d52b1d')
      .text(resultText, 50, y + 4, { align: 'center' });
  }

  doc.font('Helvetica').fontSize(28)
    .text('$' + Math.abs(refundOrOwe).toLocaleString('en-US', { minimumFractionDigits: 2 }), 50, y + 26, { align: 'center' });

  y += 70;
  doc.font('Helvetica').fontSize(8).fill('#999')
    .text('⚠️  This is an ESTIMATE only — not an official IRS filing. Consult a licensed tax professional before filing.', 50, y, { align: 'center' });

  // Try to render Arabic warning text with Arabic-compatible font
  try {
    doc.font('ArabicFallback').fontSize(8).fill('#999')
      .text('هذا تقدير فقط — مش تقديم رسمي لمصلحة الضرائب. استشر محاسب معتمد قبل التقديم.', 50, y + 12, { align: 'center' });
  } catch (err) {
    // If Arabic font not available, skip the Arabic text to avoid rendering issues
    console.warn('Arabic font unavailable; Arabic text omitted from PDF');
  }

  doc.end();
  }); // end Promise
}

module.exports = { generate };
